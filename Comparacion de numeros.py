"""
Comparación de Números: 
Escribe un programa que solicite al usuario dos números y determine 
e imprima cuál de ellos es mayor.
"""
num_1 = float(input("Ingrese el primer numero: "))
num_2 = float(input("Ingrese el segundo numero: "))

if num_1 > num_2:
    print("El primer numero es el mayor")
else:
    print("El segundo numero es el mayor")
    
